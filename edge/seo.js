// Unmatched SPA routes (typos, dead slugs, removed pages) used to fall through
// to the 200 SPA fallback and index as thin homepage duplicates. Serve them as
// real 404s with noindex instead; app.jsx renders the matching not-found view.
function notFoundSeo(pathname) {
  return {
    title: `Page not found — ${SITE_NAME}`,
    description:
      "That page does not exist on The Talus Field. The articles index, planning guide, and trip planner map are good places to reorient.",
    canonical: `${SITE_ORIGIN}${pathname}`,
    ogType: "website",
    image: SITE_DEFAULT_IMAGE,
    jsonLd: null,
    breadcrumb: null,
    faq: null,
    robots: "noindex, follow",
    status: 404,
    prose:
      `<h1>Page not found</h1><p>There is nothing at this address. The link may be old, or the page may have moved.</p>` +
      `<p>Good places to reorient: <a href="/articles">the articles index</a>, <a href="/planning">the planning guide</a>, or <a href="/map">the trip planner map</a>.</p>`,
  };
}

// Mirror of slugifyImage in components.jsx / the gen scripts: derive the
// responsive variant basename from an image path.
function slugifyImagePath(image) {
  const base = String(image).split("/").pop() || "";
  return base.toLowerCase().replace(/\.[^.]+$/, "").replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
}

// Per-route hero LCP preload (C12), mirroring index.html's home-hero preload:
// a responsive AVIF imagesrcset so the browser fetches the LCP image during HTML
// parse, ahead of React. Null for external/missing images.
function heroPreloadTag(imagePath) {
  if (!imagePath || /^https?:/i.test(imagePath)) return null;
  const cleaned = imagePath.replace(/^\/+/, "");
  const slash = cleaned.lastIndexOf("/");
  const dir = slash >= 0 ? cleaned.slice(0, slash) : "";
  const base = `/${dir ? dir + "/" : ""}responsive/${slugifyImagePath(cleaned)}`;
  const srcset = [400, 800, 1200, 1600].map((w) => `${base}-${w}.avif ${w}w`).join(", ");
  return `<link rel="preload" as="image" type="image/avif" imagesrcset="${srcset}" imagesizes="(max-width: 700px) 100vw, 700px" fetchpriority="high" />`;
}

function escapeHtmlText(s) {
  return String(s).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

export default {
  fetch(request, env) {
    // The asset layer answers requests that match real files before the
    // Worker runs, so `next()` here is always the SPA-fallback index.html
    // (or a 404-shaped asset response) — the same thing Pages' context.next()
    // returned. Keeping the old handler shape makes the port a two-line diff.
    return handleRequest({ request, env, next: () => env.ASSETS.fetch(request) });
  },
};

// Named export for offline checks (scripts/, plain node): lets a test call the
// route metadata logic without workerd's HTMLRewriter runtime.
export { seoForPath };

// Permanent redirects, checked before any SEO work. The site had no redirect
// capability before the evergreen event pages landed (it is a Worker, not
// Pages, so there is no _redirects file); this table is the mechanism. Add an
// entry when a URL is retired or consolidated — e.g. if a year-stamped article
// slug ever folds into its evergreen event page — and crawlers get a real 301
// instead of a 404 or a duplicate.
const REDIRECTS = {
  // "/articles/some-retired-slug": "/firefall",
};

async function handleRequest({ request, next, env }) {
  const url = new URL(request.url);
  const redirectTarget = REDIRECTS[url.pathname.replace(/\/+$/, "") || "/"];
  if (redirectTarget) {
    return Response.redirect(`${SITE_ORIGIN}${redirectTarget}`, 301);
  }
  // Unknown SPA routes get a real 404 (with noindex + not-found prose) instead
  // of a 200 homepage clone. Real files never reach the Worker (asset-first
  // routing), so anything unmatched here is genuinely not a page.
  const seo = seoForPath(url.pathname, url.searchParams) || notFoundSeo(url.pathname);

  const response = await next();
  const ct = response.headers.get("content-type") || "";
  if (!ct.toLowerCase().includes("text/html")) return response;

  // Crawler-visible body text. Article routes fetch the committed prerendered
  // prose fragment (prefixed with the article <h1>); hub/section/not-found
  // routes carry their own fragment in seo.prose, <h1> included. Fails open:
  // any miss just leaves the SPA-only #root.
  let proseHtml = null;
  if (seo.prerenderSlug && env && env.ASSETS) {
    try {
      const pr = await env.ASSETS.fetch(new URL(`/prerender/${seo.prerenderSlug}.html`, url.origin));
      if (pr.ok) proseHtml = `<h1>${escapeHtmlText(seo.articleTitle || "")}</h1>` + (await pr.text());
    } catch (_e) { /* fail open: no injected prose */ }
  } else if (seo.prose) {
    proseHtml = seo.prose;
  }
  const heroPreload = seo.prerenderSlug ? heroPreloadTag(seo.heroImagePath) : null;
  // Route-aware static H1: the sr-only #seo-static-h1 in index.html carries the
  // homepage title for markup-only parsers. When a prose fragment is injected
  // it brings its own on-topic <h1>, so the static one is removed to avoid a
  // duplicate; otherwise it is rewritten to the route's title (site name
  // suffix stripped) so no route ships a topic-mismatched heading.
  const staticH1Text = proseHtml ? null : seo.title.replace(/\s+—\s+The Talus Field$/, "");

  const rewritten = new HTMLRewriter()
    .on("title", {
      element(el) {
        el.setInnerContent(seo.title);
      },
    })
    .on('meta[name="description"]', {
      element(el) {
        el.setAttribute("content", seo.description);
      },
    })
    .on('meta[name="twitter:title"]', {
      element(el) {
        el.setAttribute("content", seo.title);
      },
    })
    .on('meta[name="twitter:description"]', {
      element(el) {
        el.setAttribute("content", seo.description);
      },
    })
    .on('meta[name="twitter:image"]', {
      element(el) {
        el.setAttribute("content", seo.image);
      },
    })
    .on('meta[property="og:title"]', {
      element(el) {
        el.setAttribute("content", seo.title);
      },
    })
    .on('meta[property="og:description"]', {
      element(el) {
        el.setAttribute("content", seo.description);
      },
    })
    .on('meta[property="og:url"]', {
      element(el) {
        // Trip-share overrides set ogUrl to the full shared URL so scrapers
        // unfurl the specific trip; canonical stays the clean route.
        el.setAttribute("content", seo.ogUrl || seo.canonical);
      },
    })
    .on('meta[property="og:type"]', {
      element(el) {
        el.setAttribute("content", seo.ogType);
      },
    })
    .on('meta[property="og:image"]', {
      element(el) {
        el.setAttribute("content", seo.image);
      },
    })
    .on('meta[property="og:image:width"]', {
      element(el) {
        if (seo.imageWidth) el.setAttribute("content", String(seo.imageWidth));
      },
    })
    .on('meta[property="og:image:height"]', {
      element(el) {
        if (seo.imageHeight) el.setAttribute("content", String(seo.imageHeight));
      },
    })
    .on('link[rel="canonical"]', {
      element(el) {
        el.setAttribute("href", seo.canonical);
      },
    })
    .on('meta[name="robots"]', {
      element(el) {
        if (seo.robots) el.setAttribute("content", seo.robots);
      },
    })
    .on('meta[property="og:image:alt"]', {
      element(el) {
        if (seo.imageAlt) el.setAttribute("content", seo.imageAlt);
      },
    })
    .on("head", {
      element(el) {
        if (seo.articleOg) {
          const og = seo.articleOg;
          if (og.publishedTime) {
            el.append(
              `<meta property="article:published_time" content="${og.publishedTime}" />`,
              { html: true }
            );
          }
          if (og.modifiedTime) {
            el.append(
              `<meta property="article:modified_time" content="${og.modifiedTime}" />`,
              { html: true }
            );
          }
          if (og.author) {
            el.append(
              `<meta property="article:author" content="${og.author}" />`,
              { html: true }
            );
          }
          if (og.section) {
            el.append(
              `<meta property="article:section" content="${og.section}" />`,
              { html: true }
            );
          }
        }
        if (seo.jsonLd) {
          el.append(
            `<script type="application/ld+json" id="ld-page">${safeJsonForScript(seo.jsonLd)}</script>`,
            { html: true }
          );
        }
        if (seo.breadcrumb) {
          el.append(
            `<script type="application/ld+json" id="ld-breadcrumb">${safeJsonForScript(seo.breadcrumb)}</script>`,
            { html: true }
          );
        }
        if (seo.faq) {
          el.append(
            `<script type="application/ld+json" id="ld-faq">${safeJsonForScript(seo.faq)}</script>`,
            { html: true }
          );
        }
        if (seo.trail) {
          el.append(
            `<script type="application/ld+json" id="ld-trail">${safeJsonForScript(seo.trail)}</script>`,
            { html: true }
          );
        }
        if (heroPreload) {
          el.append(heroPreload, { html: true });
        }
      },
    })
    .on("#seo-static-h1", {
      element(el) {
        if (proseHtml) el.remove();
        else if (staticH1Text) el.setInnerContent(staticH1Text);
      },
    })
    .on("#root", {
      element(el) {
        // Inject the prose fragment (article body or hub summary, <h1>
        // included) as the first child of #root so non-JS crawlers see
        // route-specific text. The client boots with createRoot().render()
        // which replaces #root's children, and app.jsx also removes
        // #prerender-prose on boot, so JS users only ever see React's copy.
        if (proseHtml) {
          el.prepend(`<div id="prerender-prose">${proseHtml}</div>`, { html: true });
        }
      },
    })
    .transform(response);

  // Not-found routes ship the SPA shell (so the client can render its own
  // not-found view) but with a real 404 status for crawlers and link checkers.
  if (seo.status === 404) {
    return new Response(rewritten.body, {
      status: 404,
      statusText: "Not Found",
      headers: rewritten.headers,
    });
  }
  return rewritten;
}
