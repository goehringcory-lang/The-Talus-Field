// Shared-trip unfurls: /map?trip=id1,id2 links travel through texts and group
// chats, and the card is the click. Overrides title/description/og:image with
// the trip's stop count and first stop names (validated against the
// trip-points.json mirror; unknown ids are dropped, an all-invalid trip falls
// back to the generic /map card). `ogUrl` carries the full shared URL so
// scrapers unfurl the specific trip, while canonical stays a clean /map and
// search consolidates there. Cards are pre-generated by scripts/gen-og-cards.mjs.
const TRIP_OG_CARD_MAX = 20;
function applyTripSeo(seo, searchParams) {
  const trip = searchParams && searchParams.get("trip");
  if (!trip) return;
  const ids = [...new Set(trip.split(",").map((s) => s.trim()))].filter((id) =>
    Object.prototype.hasOwnProperty.call(tripPoints, id)
  );
  if (!ids.length) return;
  const n = ids.length;
  const names = ids.slice(0, 2).map((id) => tripPoints[id]);
  const rest = n - names.length;
  const stopList = rest > 0 ? `${names.join(", ")}, and ${rest} more` : names.join(", ");
  seo.title = `A ${n}-stop Yosemite trip — ${SITE_NAME}`;
  seo.description = `${stopList}, in drive order. A shared trip on the Talus Field planner map, curated by a resident of the park. Open it to see the route.`;
  seo.image = `${SITE_ORIGIN}/img/og/trip-${Math.min(n, TRIP_OG_CARD_MAX)}.jpg`;
  seo.ogUrl = `${SITE_ORIGIN}/map?trip=${ids.join(",")}`;
}

